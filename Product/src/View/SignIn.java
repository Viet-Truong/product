    private void SignInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignInBtnActionPerformed
        // TODO add your handling code here:
        String userName = this.userName.getText();
        String passWord = this.passWord.getText();
        try {
            if(accountService.getAccount(userName, passWord) != null && check.isSelected()){
                JOptionPane.showMessageDialog(SignIn.this, "Dang nhap thanh cong", "Thanh cong", JOptionPane.PLAIN_MESSAGE);
                setGetUserName(userName);
                new ProductFrame(userName).setVisible(true);
                this.dispose();
            }
            else if(!check.isSelected()){
                SignInBtn.setSelected(false);
                JOptionPane.showMessageDialog(SignIn.this, "Xac nhan k thanh cong", "Thanh cong", JOptionPane.PLAIN_MESSAGE);
            } else{
                SignInBtn.setSelected(false);
                JOptionPane.showMessageDialog(SignIn.this, "Ten dang nhap hoac mat khau sai", "loi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(SignIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SignInBtnActionPerformed

